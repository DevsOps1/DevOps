using System;
using System.Collections.Generic;
using System.Text;

namespace Projet_Calculatrice
{
    public class calc
    {
        public calc()
        {
        }

        public int add(int a, int b)
        {
            return a + b;
        }

        public int moins(int a, int b)
        {
            return a - b;
        }
        public int Multi(int a, int b)
        {
            return a * b;
        }
        public int div(int a, int b)
        {
            return a / b;
        }

    }
}
